		<li class="poem title"><h2 class="poem">Solstice 2</h2></li>
		<li class="poem line">we come</li>
		<li class="poem line">in so many disguises,</li>
		<li class="poem line">and buried in silence</li>
		<li class="poem line">things would talk to us</li>
		<li class="poem line">if they thought we would listen</li>
		<li class="poem line">&nbsp;</li>
		<li class="poem line">here is a mask</li>
		<li class="poem line">behind is the hidden face of winter</li>
		<li class="poem line">and cold like a smack</li>
		<li class="poem line">an open hand across a cheek:</li>
		<li class="poem line">the scalding brands of compulsion</li>
		<li class="poem line">&nbsp;</li>
		<li class="poem line">these are the things we do know:</li>
		<li class="poem line">my hands are grey, not white or brown</li>
		<li class="poem line">and they reach, naked</li>
		<li class="poem line">towards those pregnant clouds,</li>
		<li class="poem line">silken shrouds shift lower, and then lower still</li>
		<li class="poem line">wrapping us like gifts</li>
		<li class="poem line">&nbsp;</li>
		<li class="poem line">all we are is come to this</li>
		<li class="poem line">clutching daylight, drawing power</li>
		<li class="poem line">batteries recharging in the shortness of hours</li>
		<li class="poem line">and darkness broken by naïve exuberance</li>
		<li class="poem line">&nbsp;</li>
		<li class="poem line">we do not hear the crack of an empty wood -</li>
		<li class="poem line">how do we know what we know?</li>
		<li class="poem line">&nbsp;</li>
		<li class="poem line">- it is given.</li>
		<li class="poem line">a compensation, a perk of the loneliness of night</li>
		<li class="poem line">and as we leave our white footprints,</li>
		<li class="poem line">mourn the loss of the tickle of grass</li>
		<li class="poem line">and huddle indoors in the glow of private flames</li>
		<li class="poem line">we are yet bidden</li>
		<li class="poem line">Do me honour.</li>
		<li class="poem line">for your final disguise is this:</li>
		<li class="poem line">you are children, and you must laugh</li>
		<li class="poem line">and fire is the father of you all.</li>
