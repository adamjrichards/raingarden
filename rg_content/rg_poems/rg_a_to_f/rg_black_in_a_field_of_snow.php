	##ul class='poem' id='the_black_in_a_field_of_snow_poem_body'>
		##li class='poem title'>##h2>Black, in a field of snow##/h2>##/li>
		##li class='poem line'>i am only somewhat evil##/li>
		##li class='poem line spacer'>&nbsp;##/li>
		##li class='poem line'>there is no more than a dark underpaint##/li>
		##li class='poem line'>that shows through the cracks##/li>
		##li class='poem line'>a hint of black in a field of snow##/li>
		##li class='poem line spacer'>&nbsp;##/li>
		##li class='poem line'>for all of my species contain this pollution##/li>
		##li class='poem line'>grasping fingers of self##/li>
		##li class='poem line'>entwined in the hair of our common desire##/li>
		##li class='poem line spacer'>&nbsp;##/li>
		##li class='poem line'>by nature, the subtle greed##/li>
		##li class='poem line'>that motivates both higher and lower callings##/li>
		##li class='poem line'>betrays the altruist:##/li>
		##li class='poem line'>i do what i do for love##/li>
		##li class='poem line'>and this is for no benefit but mine##/li>
		##li class='poem line spacer'>&nbsp;##/li>
		##li class='poem line'>we are a bird##/li>
		##li class='poem line'>beautiful and soft##/li>
		##li class='poem line'>yet##/li>
		##li class='poem line'>programmed by instinct and selfish genes##/li>
		##li class='poem line'>i love you, as only evil allows##/li>
		##li class='poem line'>and purity would sicken me##/li>
		##li class='poem line'>leave me vague with anger##/li>
		##li class='poem line'>i would follow you in the darkness##/li>
		##li class='poem line'>were you not to come to me in the day##/li>
